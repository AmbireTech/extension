  // 3. Keeping the legacy origin check as defense-in-depth.
  //
  // The token is the primary defense - cross-origin iframes can't read main-frame
  // globals, so any iframe call to the bridge will lack the token and be rejected
  // on the RN side in handleMessage.
  //
  // useMemo with [bridgeToken] dep guarantees the script always reflects the same
  // token used by the React verification (bridgeToken never changes after mount).
  const jsBridgeHarden = useMemo(
    () => `
    (function() {
      var AMBIRE_TOKEN = ${JSON.stringify(bridgeToken)};
      var pageOrigin = window.location.origin;

      function safeStartsWith(str, search) {
        if (typeof str !== 'string' || typeof search !== 'string') return false;
        for (var i = 0; i < search.length; i++) {
          if (str[i] !== search[i]) return false;
        }
        return true;
      }

      function addTokenAndValidate(msg) {
        try {
          var json = JSON.parse(msg);
          // Defense-in-depth: legacy origin check based on payload.origin
          if (json && json.payload && json.payload.origin) {
            var msgOrigin = json.payload.origin;
            if (!(pageOrigin === msgOrigin ||
                  pageOrigin === msgOrigin + '/' ||
                  safeStartsWith(msgOrigin, pageOrigin + '/'))) {
              console.warn('[Ambire JSBridgeHarden] Origin mismatch: expected ' + pageOrigin + ', got ' + msgOrigin);
              return null;
            }
          }
          // Primary defense: stamp the token so RN can verify the message came
          // from the main frame's wrapped bridge (cross-origin iframes can't read it).
          json.__ambireToken = AMBIRE_TOKEN;
          return JSON.stringify(json);
        } catch (e) {
          // Non-JSON message - pass through unchanged. RN will reject if it lacks token.
          return msg;
        }
      }

      // 1. Wrap window.ReactNativeWebView.postMessage (Android primary, iOS shim)
      if (window.ReactNativeWebView) {
        try {
          var originalPostMessage = window.ReactNativeWebView.postMessage.bind(window.ReactNativeWebView);
          function wrappedPostMessage(msg) {
            var stamped = addTokenAndValidate(msg);
            if (stamped === null) return; // origin check rejected
            return originalPostMessage(stamped);
          }
          window.ReactNativeWebView = new Proxy(window.ReactNativeWebView, {
            get: function(target, prop) {
              if (prop === 'postMessage') return wrappedPostMessage;
              var val = target[prop];
              return typeof val === 'function' ? val.bind(target) : val;
            }
          });
        } catch (e) {
          console.error('[Ambire JSBridgeHarden] Failed to wrap ReactNativeWebView:', e);
        }
      }

      // 2. Wrap iOS WK API. On iOS, react-native-webview's window.ReactNativeWebView.postMessage
      // is a thin shim over window.webkit.messageHandlers.ReactNativeWebView.postMessage.
      // Cross-origin iframes get their own window.webkit.messageHandlers in iOS, so wrapping
      // here only affects the main frame - which is the goal: iframe calls go through
      // their own un-wrapped WK API and arrive at RN without the token.
      if (window.webkit && window.webkit.messageHandlers && window.webkit.messageHandlers.ReactNativeWebView) {
        try {
          var wkHandler = window.webkit.messageHandlers.ReactNativeWebView;
          var originalWKPostMessage = wkHandler.postMessage.bind(wkHandler);
          // Use a plain assignment (not Object.defineProperty with configurable:false) so
          // we don't break legitimate code that may need to introspect/replace handlers.
          wkHandler.postMessage = function(msg) {
            var stamped = addTokenAndValidate(msg);
            if (stamped === null) return; // origin check rejected
            return originalWKPostMessage(stamped);
          };
        } catch (e) {
          console.error('[Ambire JSBridgeHarden] Failed to wrap WK API:', e);
        }
      }
    })();
  `,
    [bridgeToken]
  )

