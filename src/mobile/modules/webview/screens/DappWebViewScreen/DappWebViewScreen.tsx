// SECURITY: Stash native references before any page JS can overwrite them.
// injectJavaScript (used for responses/broadcasts) runs AFTER page JS, so
// downstream injections must reach these pre-captured refs rather than the
// (potentially overwritten) globals like window.postMessage or window.dispatchEvent.
// Stored on a non-enumerable, non-configurable, frozen descriptor so the
// page cannot delete or reassign the stash itself.
const jsNativeStash = `
  (function() {
    var stash = Object.freeze({
      postMessage: window.postMessage.bind(window),
      dispatchEvent: window.dispatchEvent.bind(window)
    });
    Object.defineProperty(window, '__ambireNative', {
      value: stash,
      writable: false,
      configurable: false,
      enumerable: false
    });
  })();
`

// Dev-only WebView helpers
const devOnlyHelpers = `
  // Forward console output to React Native for easy debugging
  (function() {
    var originalLog = console.log;
    var originalWarn = console.warn;
    var originalError = console.error;

    // Expand objects that JSON.stringify can't see: Events (esp. CloseEvent / WebSocket
    // error events) and Errors. Without this, the webpack-dev-server HMR client surfaces
    // ws errors as the useless "{\\"isTrusted\\":true}" because Event props are getters,
    // not own enumerable keys.
    function describeArg(arg) {
      if (arg === null) return 'null';
      if (arg === undefined) return 'undefined';
      if (typeof arg !== 'object') return String(arg);

      if (arg instanceof Error) {
        return JSON.stringify({
          __type: arg.name || 'Error',
          message: arg.message,
          stack: arg.stack
        });
      }

      // CloseEvent (extends Event) — what the WebSocket fires on disconnect.
      if (typeof CloseEvent !== 'undefined' && arg instanceof CloseEvent) {
        var t = arg.target || {};
        return JSON.stringify({
          __type: 'CloseEvent',
          type: arg.type,
          code: arg.code,
          reason: arg.reason,
          wasClean: arg.wasClean,
          targetUrl: t.url,
          targetReadyState: t.readyState
        });
      }

      // Generic Event (e.g. WebSocket 'error' event — code/reason not available).
      if (typeof Event !== 'undefined' && arg instanceof Event) {
        var et = arg.target || {};
        return JSON.stringify({
          __type: arg.constructor && arg.constructor.name || 'Event',
          type: arg.type,
          targetUrl: et.url,
          targetReadyState: et.readyState,
          isTrusted: arg.isTrusted
        });
      }

      try {
        return JSON.stringify(arg);
      } catch(e) {
        return String(arg);
      }
    }

    function sendToRN(type, args) {
      window.ReactNativeWebView.postMessage(JSON.stringify({
        type: 'log',
        logType: type,
        args: Array.from(args).map(describeArg)
      }));
    }

    console.log = function() {
      sendToRN('log', arguments);
      originalLog.apply(console, arguments);
    };
    console.warn = function() {
      sendToRN('warn', arguments);
      originalWarn.apply(console, arguments);
    };
    console.error = function() {
      sendToRN('error', arguments);
      originalError.apply(console, arguments);
    };
  })();

  // Surface uncaught page errors via the same pipe
  window.onerror = function(msg, url, lineNo, columnNo, error) {
    var errStr = error ? (error.stack || error.message || String(error)) : msg;
    console.error('[Ambire window.onerror]', errStr, 'at', url, ':', lineNo, ':', columnNo);
    return false;
  };

  // Surface unhandled promise rejections — webpack-dev-server fetch failures often
  // land here instead of window.onerror.
  window.addEventListener('unhandledrejection', function(ev) {
    var reason = ev && ev.reason;
    var detail = reason instanceof Error
      ? (reason.stack || reason.message || String(reason))
      : (function() { try { return JSON.stringify(reason); } catch(e) { return String(reason); } })();
    console.error('[Ambire unhandledrejection]', detail);
  });

  // Hide dev error overlays (react-error-overlay, webpack-dev-server, Next.js dev)
  // The document may not be fully loaded

  document.addEventListener('DOMContentLoaded', function() {
    var s = document.createElement('style');
    s.textContent = '#__react-error-overlay__,iframe#webpack-dev-server-client-overlay,nextjs-portal{display:none!important}';
    (document.head || document.documentElement).appendChild(s);
  });
`

